// Является ли буква русского алфавита гласной
 #include <stdio.h>
#include <ctype.h>
#include <string.h>

int main() {
    // Массивы гласных и согласных букв русского алфавита
    char vowels[] = "аеёиоуыэюяАЕЁИОУЫЭЮЯ";
    char consonants[] = "бвгджзйклмнпрстфхцчшщБВГДЖЗЙКЛМНПРСТФХЦЧШЩ";

    char ch;
    printf("Введите букву русского алфавита: ");
    scanf("%c", &ch);

    // Проверяем, является ли символ буквой
    if (!((ch >= 'А' && ch <= 'я') || ch == 'ё' || ch == 'Ё')) {
        printf("Это не буква русского алфавита.\n");
        return 0;
    }

    // Проверяем, является ли буква гласной
    if (strchr(vowels, ch) != NULL) {
        printf("Буква '%c' является гласной.\n", ch);
    }
    // Проверяем, является ли буква согласной
    else if (strchr(consonants, ch) != NULL) {
        printf("Буква '%c' является согласной.\n", ch);
    } else {
        printf("Ошибка: не удалось определить тип буквы.\n");
    }

    return 0;
}
